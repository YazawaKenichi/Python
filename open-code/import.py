'''
    import の話
    関数：def で定義して 関数名() というようにして実行する。
    モジュール：関数をいくつか集めて一つのファイルに記述したもの。そのファイル名から拡張子 .py を除いたものがモジュール名。
    パッケージ：モジュールをいくつか含めたディレクトリをパッケージとしたもの。パッケージの中にパッケージを入れる事もできる。ディレクトリ名がパッケージ名となり、ドットで区切って指定する。
    ライブラリ：関数・モジュール・パッケージの総省。または、いくつかのパッケージをまとめて一つのライブラリとしてインストールするようにしたもの。
'''